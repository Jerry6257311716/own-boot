void uart_init(void)
{
	S5PV210_UART *uart = (S5PV210_UART *)(0xE2900000);//uart0

	__REG(0xE0200000) = 0x2222;//config GPA0_0/1/2/3 as uart mode

	uart->UFCON = 0x0;
	uart->UMCON = 0x0;
	uart->ULCON = 0x3;		//data=8bit
	uart->UCON = 0x3c5;		//polling
	uart->UBRDIV = 34;
	uart->UDIVSLOT = 0xdddd;
	uart->UTXH= 'A';
}


